# Episode Models
# =============================================================================

class EpisodeSummary(BaseModel):
    """Summary of an episode in the series outline."""
    episode_number: int = Field(..., ge=1, description="Episode number (1-indexed)")
    title: str = Field(..., description="Episode title")
    premise: str = Field(..., description="Episode premise/summary")
    key_points: List[str] = Field(default_factory=list, description="Key points to cover")
    cliffhanger_hint: Optional[str] = Field(None, description="Hint about cliffhanger")
    cliffhanger_type: Optional[CliffhangerType] = Field(None, description="Planned cliffhanger type")
    callbacks_to_plant: List[str] = Field(default_factory=list, description="Callbacks to plant")
    callbacks_to_resolve: List[str] = Field(default_factory=list, description="Callbacks to resolve")


class Episode(BaseModel):
    """Full episode data after generation."""
    id: str = Field(..., description="Unique episode ID")
    series_id: str = Field(..., description="Parent series ID")
    episode_number: int = Field(..., ge=1)
    title: str = Field(...)
    status: EpisodeStatus = Field(EpisodeStatus.PENDING)

    # Job tracking
    job_id: Optional[str] = Field(None, description="Pipeline job ID")

    # Content
    previously_on: Optional[str] = Field(None, description="Previously on narration")
    cliffhanger: Optional[str] = Field(None, description="Episode cliffhanger text")
    cliffhanger_type: Optional[CliffhangerType] = Field(None)
    teaser: Optional[str] = Field(None, description="Next episode teaser")

    # Callbacks in this episode
    callbacks_planted: List[str] = Field(default_factory=list)
    callbacks_resolved: List[str] = Field(default_factory=list)

    # Script reference
    script: Optional[Dict[str, Any]] = Field(None, description="Full enhanced script")

    # Output paths
    output_path: Optional[str] = Field(None, description="Path to output video")
    audio_path: Optional[str] = Field(None, description="Path to output audio")
    duration_seconds: Optional[float] = Field(None)

    # Timestamps
    created_at: datetime = Field(default_factory=datetime.utcnow)
    completed_at: Optional[datetime] = Field(None)


