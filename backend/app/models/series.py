class SeriesOutline(BaseModel):
    """
    The planned outline for a series before generation.

    Generated by SeriesPlannerAgent and approved by user before
    episode generation begins.
    """
    title: str = Field(..., description="Series title")
    description: str = Field("", description="Series description/logline")
    episode_count: int = Field(..., ge=3, le=20, description="Number of episodes")
    episode_length: EpisodeLength = Field(EpisodeLength.SHORT)
    series_type: SeriesType = Field(SeriesType.DOCUMENTARY)

    # Narrative structure
    overall_arc: str = Field("", description="Description of overall narrative arc")
    themes: List[str] = Field(default_factory=list, description="Core themes")
    episodes: List[EpisodeSummary] = Field(default_factory=list, description="Episode summaries")

    # Style DNA
    style_dna: StyleDNA = Field(default_factory=StyleDNA)


class SeriesAssets(BaseModel):
    """Generated audio assets for series identity."""
    intro_jingle_path: Optional[str] = Field(None, description="Series intro jingle")
    outro_tag_path: Optional[str] = Field(None, description="Series outro tag")
    previously_on_bed_path: Optional[str] = Field(None, description="Previously on music bed")
    transition_rise_path: Optional[str] = Field(None, description="Transition audio")

    # Cliffhanger stings
    cliffhanger_stings: Dict[str, str] = Field(
        default_factory=dict,
        description="Cliffhanger type -> audio path"
    )

    # Emotional motifs
    motifs: Dict[str, str] = Field(
        default_factory=dict,
        description="Motif name -> audio path"
    )

    generated_at: Optional[datetime] = Field(None)


class Series(BaseModel):
    """
    Complete series state including outline, episodes, and continuity.

    This is the main data structure for tracking a series through
    its entire lifecycle from draft to completion.
    """
    id: str = Field(..., description="Unique series ID")
    status: SeriesStatus = Field(SeriesStatus.DRAFT)

    # User input
    prompt: str = Field(..., description="Original user prompt")
    guidance: Optional[str] = Field(None, description="Additional user guidance")
    mode: str = Field("normal", description="Pipeline mode for episodes")

    # Outline (generated, then potentially modified)
    outline: SeriesOutline = Field(..., description="Series outline")

    # Episodes (populated as generated)
    episodes: List[Episode] = Field(default_factory=list)

    # Continuity tracking
    continuity_state: ContinuityState = Field(default_factory=ContinuityState)

    # Series assets
    assets: Optional[SeriesAssets] = Field(None)
    assets_path: Optional[str] = Field(None, description="Base path for series assets")

    # Timestamps
    created_at: datetime = Field(default_factory=datetime.utcnow)
    approved_at: Optional[datetime] = Field(None)
    completed_at: Optional[datetime] = Field(None)

    @property
    def current_episode(self) -> int:
        """Get the next episode to generate."""
        completed = sum(1 for ep in self.episodes if ep.status == EpisodeStatus.COMPLETED)
        return completed + 1

    @property
    def is_complete(self) -> bool:
        """Check if all episodes are generated."""
        return len(self.episodes) >= self.outline.episode_count and all(
            ep.status == EpisodeStatus.COMPLETED for ep in self.episodes
        )

    @property
    def progress_percent(self) -> float:
        """Get overall series completion percentage."""
        if not self.outline.episode_count:
            return 0
        completed = sum(1 for ep in self.episodes if ep.status == EpisodeStatus.COMPLETED)
        return (completed / self.outline.episode_count) * 100
